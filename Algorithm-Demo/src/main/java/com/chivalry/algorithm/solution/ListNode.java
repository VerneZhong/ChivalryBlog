package com.chivalry.algorithm.solution;

/**
 * Definition for singly-linked list.
 * @author verne.zhong
 * @description
 * @date 2023/09/15 21:44
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
